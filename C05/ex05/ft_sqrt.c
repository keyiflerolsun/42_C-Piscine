int	ft_sqrt(int nb)
{
	int	tmp;

	tmp = 1;
	while (tmp * tmp <= nb)
		tmp++;
	tmp = tmp - 1;
	if (tmp * tmp == nb)
		return (tmp);
	return (0);
}
